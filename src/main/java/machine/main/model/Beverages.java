public class Beverages {
    private HotTea hot_tea;
    private HotCoffee hot_coffee;
    private BlackTea black_tea;
    private GreenTea green_tea;

    public HotTea getHot_tea() {
        return hot_tea;
    }

    public void setHot_tea(HotTea hot_tea) {
        this.hot_tea = hot_tea;
    }

    public HotCoffee getHot_coffee() {
        return hot_coffee;
    }

    public void setHot_coffee(HotCoffee hot_coffee) {
        this.hot_coffee = hot_coffee;
    }

    public BlackTea getBlack_tea() {
        return black_tea;
    }

    public void setBlack_tea(BlackTea black_tea) {
        this.black_tea = black_tea;
    }

    public GreenTea getGreen_tea() {
        return green_tea;
    }

    public void setGreen_tea(GreenTea green_tea) {
        this.green_tea = green_tea;
    }
}
